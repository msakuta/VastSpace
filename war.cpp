Entity *WarField::addent(Entity *e){
	Entity **plist = e->isTargettable() ? &el : &bl;
	e->w = this;
	e->next = *plist;
	return *plist = e;
}

Player *WarField::getPlayer(){
	if(pl)
		return pl;
	CoordSys *root = this->cs->findcspath("/");
	if(root && root->toUniverse())
		return root->toUniverse()->ppl;
}






























